        //   $model = Orders::find()->MAX('order_no');
        $model = Quotation::find()->MAX('quotation_no');

        $pre = "QO";

        if ($model != null) {
//            $prefix = $pre.substr(date("Y"),2,2);
//            $cnum = substr((string)$model,4,strlen($model));
//            $len = strlen($cnum);
//            $clen = strlen($cnum + 1);
